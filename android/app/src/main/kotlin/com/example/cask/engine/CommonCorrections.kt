    /** Generally useful next-word fillers when bigram/trigram data is unavailable (priority first). */
    val COMMON_NEXT = listOf(
        "the", "to", "a", "I", "you", "and", "is", "it", "that", "of",
        "for", "in", "on", "be", "have", "do", "we", "are", "this", "with",
        "not", "but", "so", "just", "can", "will", "was", "what", "out", "up",
    )

    /**
     * Lowercased set of words the predictor is allowed to surface even when they aren't dictionary
     * entries (e.g. `i'm`). Built from the curated prediction lists — never from the typo keys — so a
     * misspelling can never leak into predictions.
     */
    val PREDICTION_WHITELIST: Set<String> =
        (SENTENCE_STARTERS + COMMON_NEXT).mapTo(HashSet()) { it.lowercase() }
