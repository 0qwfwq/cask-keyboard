/**
 * A curated, hand-tuned table of the corrections a statistical model can't make on its own, plus a
 * little common-sense language data for predictions. It exists because the shipped frequency list
 * (Norvig's `count_1w`, web-derived) contains thousands of *misspellings as high-frequency entries* —
 * `im` (37M), `dont` (20M), `cant`, `wont`, `teh`, `recieve`, `alot`, `definately`, … all look like
 * perfectly good words to a pure dictionary lookup. The noisy-channel model therefore never touches
 * them ("it's already a known word"), which is the single biggest reason naive autocorrect feels dumb.
 *
 * This table fixes the most common offenders directly and, crucially, **overrides dictionary
 * membership**: a [Fix] is applied even when the typed string is itself in the dictionary. Two tiers:
 *
 *  - [Fix.auto] `true`  — the typed form is essentially never intended (`dont`, `recieve`, `teh`); it
 *    is auto-applied at the word boundary (and still one-tap revertable).
 *  - [Fix.auto] `false` — ambiguous, because the typed form is also a common real word (`its`, `were`,
 *    `well`, `lets`, `id`); it is only *offered* at the top of the suggestion strip, never forced.
 *
 * Corrections carry their own canonical capitalization/punctuation (`I'm`, `don't`, `a lot`); the
 * engine re-cases them to match how the user typed (see `CorrectionEngine.recaseCurated`). Anything
 * the user reverts is remembered in [PersonalStore] and never re-applied.
 */
object CommonCorrections {

    /** A correction target. [to] is the canonical display form; [auto] gates auto-application. */
    data class Fix(val to: String, val auto: Boolean)

    private val map: HashMap<String, Fix> = HashMap(512)

    /** The correction for [typedLower] (already lowercased), or `null` if none is curated. */
    fun lookup(typedLower: String): Fix? = map[typedLower]

    fun isKnownCorrection(typedLower: String): Boolean = map.containsKey(typedLower)

    /** True if [typedLower] is a curated *auto* typo (e.g. `dont`, `im`) — never predict/suggest it. */
    fun isAutoTypo(typedLower: String): Boolean = map[typedLower]?.auto == true

